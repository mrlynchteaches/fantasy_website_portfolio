const overlay = document.getElementById('modalOverlay');
const modalTitle = document.getElementById('modalTitle');
const modalBody = document.getElementById('modalBody');
const closeModalButton = document.getElementById('closeModal');
const buttons = Array.from(document.querySelectorAll('.poi'));
const statusBox = document.getElementById('statusBox');
const avatar = document.getElementById('avatar');
const toggleAvatarButton = document.getElementById('toggleAvatarBtn');
const mapStage = document.getElementById('mapStage');
let lastTrigger = null;

const avatarState = {
  enabled: false,
  x: 50,
  y: 50,
  speed: 0.35,
  keys: { up: false, down: false, left: false, right: false },
  nearestPoi: null,
  rafId: null
};

function openModal(key, trigger) {
  const entry = entries[key];
  if (!entry) return;
  modalTitle.textContent = entry.title;
  modalBody.innerHTML = entry.body;
  overlay.hidden = false;
  overlay.classList.add('open');
  document.body.style.overflow = 'hidden';
  lastTrigger = trigger || null;
  statusBox.innerHTML = `
    <h3>${entry.title} opened</h3>
    <p>Well done traveler, you have entered <strong>${entry.title}</strong>. Scroll inside the panel to read longer text, then close it to return to the map.</p>
  `;
  closeModalButton.focus();
}

function closeModal() {
  overlay.classList.remove('open');
  overlay.hidden = true;
  document.body.style.overflow = '';
  if (lastTrigger) lastTrigger.focus();
}

function updateAvatarPosition() {
  avatar.style.left = `${avatarState.x}%`;
  avatar.style.top = `${avatarState.y}%`;
}

function resetPoiHighlights() {
  buttons.forEach((button) => button.classList.remove('poi--near'));
}

function getNearestPoi() {
  if (!avatarState.enabled) {
    avatarState.nearestPoi = null;
    resetPoiHighlights();
    return;
  }

  let nearestButton = null;
  let nearestDistance = Infinity;

  buttons.forEach((button) => {
    const targetX = Number(button.dataset.avatarX);
    const targetY = Number(button.dataset.avatarY);
    const dx = targetX - avatarState.x;
    const dy = targetY - avatarState.y;
    const distance = Math.sqrt(dx * dx + dy * dy);
    if (distance < nearestDistance) {
      nearestDistance = distance;
      nearestButton = button;
    }
  });

  resetPoiHighlights();

  if (nearestButton && nearestDistance < 8) {
    nearestButton.classList.add('poi--near');
    avatarState.nearestPoi = nearestButton;
    statusBox.innerHTML = `
      <h3>Avatar Exploration Active</h3>
      <p>You are near <strong>${entries[nearestButton.dataset.key].title}</strong>. Press <strong>Enter</strong> to open this point of interest.</p>
      <p>Mouse clicks target the map text. Avatar Enter targeting uses the separate hidden image/icon location for this POI.</p>
    `;
  } else {
    avatarState.nearestPoi = null;
    statusBox.innerHTML = `
      <h3>Avatar Exploration Active</h3>
      <p>Move the avatar with <strong>WASD</strong> or the <strong>arrow keys</strong> until a point of interest is nearby.</p>
      <p>Mouse clicks still target the visible text labels on the map.</p>
    `;
  }
}

function tickAvatar() {
  if (!avatarState.enabled) return;
  const dx = (avatarState.keys.right ? 1 : 0) - (avatarState.keys.left ? 1 : 0);
  const dy = (avatarState.keys.down ? 1 : 0) - (avatarState.keys.up ? 1 : 0);
  avatarState.x = Math.min(Math.max(avatarState.x + dx * avatarState.speed, 2), 98);
  avatarState.y = Math.min(Math.max(avatarState.y + dy * avatarState.speed, 2), 98);
  updateAvatarPosition();
  getNearestPoi();
  avatarState.rafId = requestAnimationFrame(tickAvatar);
}

function startAvatar() {
  avatarState.enabled = true;
  avatar.classList.remove('avatar--hidden');
  avatar.setAttribute('aria-hidden', 'false');
  toggleAvatarButton.textContent = 'Disable Avatar Exploration';
  updateAvatarPosition();
  getNearestPoi();
  if (!avatarState.rafId) tickAvatar();
}

function stopAvatar() {
  avatarState.enabled = false;
  avatar.classList.add('avatar--hidden');
  avatar.setAttribute('aria-hidden', 'true');
  toggleAvatarButton.textContent = 'Enable Avatar Exploration';
  resetPoiHighlights();
  avatarState.nearestPoi = null;
  if (avatarState.rafId) {
    cancelAnimationFrame(avatarState.rafId);
    avatarState.rafId = null;
  }
  statusBox.innerHTML = `
    <h3>Map Interaction Updated</h3>
    <p>Mouse interactions target the visible text labels on the map. Avatar exploration uses separate hidden target points placed on the image/icon locations for each region.</p>
    <p><strong>Current labels:</strong> About Me, Skills, Interests, Resume, Portfolio, Blog, and Contact.</p>
  `;
}

buttons.forEach((button) => {
  button.addEventListener('click', () => openModal(button.dataset.key, button));
});
closeModalButton.addEventListener('click', closeModal);
overlay.addEventListener('click', (event) => { if (event.target === overlay) closeModal(); });

document.addEventListener('keydown', (event) => {
  if (event.key === 'Escape' && !overlay.hidden) closeModal();
  if (!avatarState.enabled) return;
  const key = event.key.toLowerCase();
  if (key === 'arrowup' || key === 'w') avatarState.keys.up = true;
  if (key === 'arrowdown' || key === 's') avatarState.keys.down = true;
  if (key === 'arrowleft' || key === 'a') avatarState.keys.left = true;
  if (key === 'arrowright' || key === 'd') avatarState.keys.right = true;
  if (event.key === 'Enter' && avatarState.nearestPoi) openModal(avatarState.nearestPoi.dataset.key, avatarState.nearestPoi);
});

document.addEventListener('keyup', (event) => {
  if (!avatarState.enabled) return;
  const key = event.key.toLowerCase();
  if (key === 'arrowup' || key === 'w') avatarState.keys.up = false;
  if (key === 'arrowdown' || key === 's') avatarState.keys.down = false;
  if (key === 'arrowleft' || key === 'a') avatarState.keys.left = false;
  if (key === 'arrowright' || key === 'd') avatarState.keys.right = false;
});

toggleAvatarButton.addEventListener('click', () => {
  if (avatarState.enabled) stopAvatar(); else startAvatar();
});

mapStage.addEventListener('mouseleave', () => {
  if (!avatarState.enabled) return;
  Object.keys(avatarState.keys).forEach((key) => { avatarState.keys[key] = false; });
});
